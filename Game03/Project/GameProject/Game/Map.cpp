Map::Map(int stage):Base(eType_Field)
{
	//画像複製
	m_img = COPY_RESOURCE("MapTip", CImage);

	//ステージの切り替え
	switch (stage) {
		//チュートリアルステージ
	case 0:
		//ステージ生成
		memcpy(m_map_data, stage0data, sizeof(stage0data));
		//プレイヤー生成
		Base::Add(new Player(CVector2D(MAP_TIP_SIZE * 3, MAP_TIP_SIZE * 20), false));
		//ゴール配置
		Base::Add(new Goal(CVector2D(MAP_TIP_SIZE * 9, MAP_TIP_SIZE * 18)));

		//スタート配置
		Base::Add(new Start(CVector2D(MAP_TIP_SIZE * 3, MAP_TIP_SIZE * 21)));

		//看板配置
		Base::Add(new Signboard(CVector2D(MAP_TIP_SIZE * 6, MAP_TIP_SIZE * 21)));
		//落ちる床配置
		Base::Add(new Fallingfloor(CVector2D(MAP_TIP_SIZE * 44, MAP_TIP_SIZE * 5)));

		//火柱の配置
		Base::Add(new FireTrap(CVector2D(MAP_TIP_SIZE * 18, MAP_TIP_SIZE * 21),false));

		break;
		//ステージ1
	case 1:
		break;
	}
}

void Map::Draw()
{
	//マップチップによる表示の繰り返し
	for (int i = 0; i < MAP_HEIGHT; i++) {
		for (int j = 0; j < MAP_WIDTH; j++) {
			//表示しない制御
			//0を表示しない制御
			if (m_map_data[i][j] == 0)continue;
			int t = m_map_data[i][j];

			//画像切り抜き
			m_img.SetRect(32*t, 0, 32*t + 32, 32);
			//表示サイズ設定
			m_img.SetSize(MAP_TIP_SIZE, MAP_TIP_SIZE);
			//表示位置設定
			m_img.SetPos(MAP_TIP_SIZE * j-m_scroll.x, MAP_TIP_SIZE * i-m_scroll.y);
			//描画
			m_img.Draw();
		}
	}
}

int Map::GetTip(const CVector2D& pos)
{
	//列を計算
	int col = pos.x / MAP_TIP_SIZE;
	//列の制限(0〜MAP_WIDTH-1)
	if (col < 0)col = 0;
	if (col > MAP_WIDTH - 1)col = MAP_WIDTH - 1;
	//行を計算
	int raw = pos.y / MAP_TIP_SIZE;
	//行の制限(0〜MAP_HEIGHT-1)
	if (raw < 0)raw = 0;
	if (raw > MAP_HEIGHT - 1)raw = MAP_HEIGHT - 1;
	//チップデータを返却
	return GetTip(col, raw);
}

int Map::GetTip(int col, int raw)
{
	return m_map_data[raw][col];
}

int Map::CollisionMap(const CVector2D& pos)
{
	//1点のみ検証
	int t = GetTip(pos);
	if (t != 0) return t;
	return 0;
}


//Mapの当たり判定
int Map::CollisionMap(const CVector2D& pos, const CRect& rect)
{
	//左上
	int t = GetTip(CVector2D(pos.x + rect.m_left, pos.y + rect.m_top));
	if (t != 0 &&t !=3)return t;
	//右上
	t = GetTip(CVector2D(pos.x + rect.m_right, pos.y + rect.m_top));
	if (t != 0 && t != 3)return t;
	//左下
	t = GetTip(CVector2D(pos.x + rect.m_left, pos.y + rect.m_bottom));
	if (t != 0 && t != 3)return t;
	//右下
	t = GetTip(CVector2D(pos.x + rect.m_right, pos.y + rect.m_bottom));
	if (t != 0 && t != 3)return t;
	return 0;
}
